# assign value to "types_of_people" variable
types_of_people = 10
# assign value to "x" variable and prints "type_of_people" value inside "x"
x = "There are #{types_of_people} types of people."
# assign value to "binary" variable
binary = 'binary'
# assign value to "do_not" variable
do_not = "don't"
# assign value to "y" variable and prints "binary" and "do_not" value inside "y"
y = "Those who know #{binary} and those who #{do_not}."

# prints "x" value
puts x
# prints "y" value
puts y

# prints a string with "x" value inside
puts "I said: #{x}."
# prints a string with "y" value inside
puts "I also said: '#{y}'."

# assign a non-string value to "hilarious" variable
hilarious = false
# assign value to "joke_evaluation" variable with "hilarious" value inside
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# print "joke_evaluation" value
puts joke_evaluation

